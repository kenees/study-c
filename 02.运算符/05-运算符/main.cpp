#include <iostream>
using namespace std;

int main() {

  // 加减乘除Ï
  int a = 10;
  int b = 3;

  cout << a + b << endl;

  cout << a - b << endl;

  cout << a * b << endl;
  // 整数相除结果还是整数
  cout << a / b << endl;

  cout << a % b << endl;

  // 小数 相除结果可以 是 小数
  cout << 5.6 / 2 << endl;
  cout << 0.6 / 0.2 << endl;

  cout << (4>3) << endl;

  cout << true << endl;
  return 0;
}